//                      所以为保持两个兵划速相同，则需要向划的慢的土兵看齐。
//                  2、两个士兵坐船时，重量增加吃水加深，水的阻力增大；
//                      同样的力量划船速度会变慢;
//                  3、由于河水湍急大的力来抵消水流的阻力，
//                      所以2)中过河用时不是a([i] *2，而是a[i] * 10
//          样例1
//              输入
//                  5
//                  43
//                  12 13 15 20 50
//              输出：
//                  3 40
//              说明：
//                  可以达到或小于43的一种方案:
//                  第一步: a[o]a[1] 过河用时: 13
//                  第二步: a[0]返回用时: 12
//                  第三步: a[0] a[2]过河用时: 15
//          样例2
//              输入
//                  5
//                  130
//                  50 12 13 15 20
//              输出：
//                  5 128
//              说明：
//                  可以达到或小于130的一种方案:
//                  第一步: a[1] a[2]过河用时: 13
//                  第二步: a[1]返回用时: 12
//                  第三步: a[0] a[4]过河用时: 50
//                  第四步: a[2] 返回用时: 13
//                  第五步: a[1] a[2]过河用时: 13
//                  第六步: a[1] 返回用时: 12
//                  第七步: a[1] a[3]过河用时: 15
//                  所以输出为: 5 128
//          样例3
//              输入
//                  7
