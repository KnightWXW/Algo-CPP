//          3、当2个士兵坐船1个兵划船时，用时为a[i]*10； a[i]为划船士兵用时。
//          4、如果士兵下河游泳，则会被湍急水流直接带走，算作死亡。
//      请帮忙给出一种解决方案；保证存活的士兵最多，且过河用时最短
//      输入输出
//          输入
//              第一行：N表示兵数(0<N<1000000)
//              第二行：T表示敌军到达时长(0< T < 10000000)
//              第三行：a[0] a[1] … a[i] … a[N- 1]
//              a[i]表示每个士兵的过河时长。
