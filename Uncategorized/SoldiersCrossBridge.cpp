//      一支N个士兵的军队正在趁夜色逃亡，途中遇到一条湍急的大河。
//      敌军在T的时长后到达河面，没过到对岸的士兵都会被消灭。
//      现在军队只找到了只小船,这船最多能同时坐上2个士兵。
//          1、当1个兵划船过河，时为a[i]; 0<=i<N
